# Data Types:
# 1) int - integer is a number without decimal part. EX: 1, 0, -12
num = 50  # integer value 50 assigned to a variable named num.
print(num)

# you can change the value of a variable anywhere, anytime.
num = 45
print(num)

# 2) str - string is a sequence of characters enclosed in a single or double quote. EX: "Harry", 'Ron'
name = "Hermoine" # str
print("I am", name)

# 3) float - floating point number is a number with decimal point. EX: 12.0, 14.4, -13.45
num1 = 12.0

# 4) bool - a Boolean is a type os value that can be either be 'True' or 'False'
is_greater = 4 > 3
print(is_greater)
